

// Primitive Types

// String

let name = "Jagan";
// name = 'Javid';
// name = `Jack`;

console.log(name);
console.log(typeof name);

// Number
const age = 27;

console.log(age);
console.log(typeof age);

// Boolean
const hadDinner = true;

console.log(hadDinner)
console.log(typeof hadDinner);

// Null
const box = null;

console.log(box);
console.log(typeof box);

// Undefined
let undef;

console.log(undef)
console.log(typeof undef)



// Reference Types

const myArr = ["Jagan", 10, true];

console.log(myArr);
console.log(typeof myArr);

// Key : Value pairs or name : value

const car = {
    name:"BMW",
    color:"Black",
    year:"2024"
};

console.log(car);
console.log(typeof car);